void print(unsigned char c, unsigned char* x, unsigned long long xlen) {
    unsigned long long i;
    puts("[");
    puthex((int)xlen);
    puts("]=");
    
    for (i = 0; i < xlen; ++i) puthex(x[i]);
    puts("\n");
}

int compare_result(unsigned char a[], unsigned char b[], unsigned long long len) {
    for(int i = 0; i < len; i++) {
        if (a[i] != b[i]) return 0;
    }
    return 1;
}


